/// <summary>
/// 스텟의 종류
/// </summary>
public enum StatType
{
    HealthPoint = 0,       // 체력
    AttackDamage,          // 공격력
    AbilityPower,          // 주문력
    AttackSpeed,           // 공격 속도
    Defense,               // 방어력
    MagicResistance,       // 마법 저항력
    ManaRegeneration,      // 마나 재생
    CriticalChance,        // 치명타 확률
    CriticalDamage,        // 치명타 피해량
    DamageIncrease,        // 가하는 피해 증가
    DamageReduction,       // 받는 피해 감소
    Omnivamp,               // 생명력 흡수
    AttackRange,           // 사거리
    MoveSpeed,             // 이동 속도
}
